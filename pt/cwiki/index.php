        <h2>序言</h2>
        <ul class="basic-ch ch0 pt-dd">
          <li class="article" id="article0">
            <h4>序言</h4>
            <p>香港自古以來就是中國的領土，一八四零年鴉片戰爭以後被英國佔領。一九八四年十二月十九日，中英兩國政府簽署了關於香港問題的聯合聲明，確認中華人民共和國政府於一九九七年七月一日恢復對香港行使主權，從而實現了長期以來中國人民收回香港的共同願望。</p>
            <p>為了維護國家的統一和領土完整，保持香港的繁榮和穩定，並考慮到香港的歷史和現實情況，國家決定，在對香港恢復行使主權時，根據中華人民共和國憲法第三十一條的規定，設立香港特別行政區，並按照「一個國家，兩種制度」的方針，不在香港實行社會主義的制度和政策。國家對香港的基本方針政策，已由中國政府在中英聯合聲明中予以闡明。</p>
            <p>根據中華人民共和國憲法，全國人民代表大會特制定中華人民共和國香港特別行政區基本法，規定香港特別行政區實行的制度，以保障國家對香港的基本方針政策的實施。</p>

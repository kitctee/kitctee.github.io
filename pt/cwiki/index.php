  <section class="basic-wrap" id="basic-wrap">
    <h1 class="basic-title" id="basic-title">現行香港特別行政區基本法</h1>
    <ul class="basic-list" id="basic-list">
      <li class="basic-inner-wrap about" id="body-about" header="關於制憲">
        <div class="basic-about pt-dd" id="basic-about">
          <h2><span class="ch">五區公投 全民制憲</span>我有權寫基本法</h2>
          <p class="lead">自九七主權移交以來，基本法帶來的爭議不斷。四次人大釋法、日益尖銳的中港矛盾證明基本法早已千瘡百孔，既不能保證港人享有普及而平等的選舉，亦無法令港人在民生及公共服務上得到政策上的保障。重新制定基本法，全民制憲，才是「港人自治」的唯一出路。以下列出數例：</p>
          <h3><span>第5條</span>只得五十年保障，香港2047前途未卜</h3>
          <p>基本法承諾一國兩制制度維持五十年不變，卻未保障2047後香港社會制度和生活方式。個人資本可能於2047後再無保障。倘若基本法到了2047年就失效，香港的法律、公民權利和義務、政府運作方式等將再無規範，我們必須盡早檢討基本法第5條，制憲保護香港。</p>
          <h3><span>第23條</span>維護國家安全為名，侵害個人表達自由</h3>
          <p>港府曾於02年提出為基本法第23條立法制定國家安全條例。立法動議當年引起強烈反對，認為法例將嚴重威脅個人表達自由和權利，被批為「白色恐怖」，逾50萬市民於03年7月1日上街「反對23條立法」，其後因自由黨「轉軚」暫時擱置。然而，近年常有中共官員及建制派人士重提舊事，要導絕後患，基本法第23條必須重新制訂。</p>
          <h3><span>第36條</span>混淆「永久居民」定義，摧毀公民社會基石</h3>
          <p>終審庭於13年底裁定綜援申請人無需居港七年，社署有關規定違反基本法。判詞指基本法第36條訂明「香港居民」享有社會福利的權利，並無列明需否為「永久性居民」。社署於短時間內接獲大量綜援申請，法律援助、公屋申請等制度亦終將受影響。這反映基本法未有清晰定義「香港居民」，更沒有承認「香港公民」的身份，令香港人身份被矮化，社會福利制度接連受到衝擊。</p>
          <h3><span>第45條</span>篩選特首修選人，操控選舉結果</h3>
          <p>基本法第45條和附件一模糊「普選」特首的定義，有違「普選」原意。政改諮詢期間，中共多次提出基本法未有列明的條件，包括「愛國愛港」等才予提名參選。「提名委員會」更是中共用以排除不合心意的參選人的灰色地帶。換言之，香港要達致普選，必須修改基本法，剷除篩選機制，才能達到港人自治。基本法早已破爛不堪，不但限制香港民主發展，更嚴重損害港人利益。只有重寫基本法，與政府重新立約，全民制憲，香港才能獨立自主地繼續發展。</p>
          <p>真普選聯盟自2013年3 月成立以來，一直強調爭取符合國際人權普及而平等的標準之餘，又不違反基本法的行政長官，以及立法會議會的選舉辦法。由於近日鬧得熱烘烘的「讓愛與和平佔領中環」運動，只討論行政長官的選舉辦法，故整個香港政治議題都集中在「提名委員會」的行政長官提名篩選機制的產生辦法之上。</p>
          <p>真普選聯盟希望提名委員會能經過全港市民投票產生，但前律政司司長暨香港特別行政區基本法委員會副主任梁愛詩則認為此想法已違反基本法。整個討論都在基本法的制紂下團團轉，泛民卻未有或迴避爭取普選要真正觸及的兩個問題：第一個問題是香港以司法獨立和法治精神引以為傲，基本法雖則奉為香港的小憲法，卻不能獨立運作，因為香港基本法背後有中共的人大常委會有干預香港的司法運作的權力，一部能被外來政權任意修改、解釋、干擾的法律，根本稱不上一個地區的小憲法。</p>
          <p>主權移交十五年來，人大常委會就已作出四次釋法，當中2004年的第二次釋法，對《基本法》附件一和附件二作出解釋，修改了「行政長官及立法會產生辦法和法案議案表決程序」。在原定的「須經立法會全體議員三分之二多數通過，行政長官同意，並報全國人大常委會批准或者備案」三步曲之前，加上額外的兩項程序：(1)行政長官就是否需要進行修改向全國人大常委會提出報告，及(2)由全國人大常委會依照《基本法》第四十五條和第六十八條規定並根據香港的實際情況和循序漸進的原則予以確定，即由「三步曲」變為「五步曲」。</p>
          <p>此外， 全國人大常委會更在釋法後一錘定音，2007年 行政長官選舉鐵定不實行普選，且2008年第四屆立法會選舉中的功能組別和直選議員比例維持不變。在中共政府只會參與香港的軍事、外交的前提下，香港的管治操作一直受外來政權侵擾，所謂的港人治港、高度自治、一國兩制，根本已是千瘡百孔。</p>
          <p>第二個問題是泛民沒有留意，其實是刻意迴避，基本法當初定明「提名委員會」的存在，就是要中共掌握行政長官產生的安全系數，泛民欲在中共能任意解釋的基本法框架底下，取走中共的安全罩門，簡直愚不可及。</p>
          <h3>泛民漠視真相的現實社運</h3>
          <p>引用斯洛文尼亞思想家齊澤克（Slavoj Zizek）反覆述說的一個笑話：一個人在路燈下找他丟失的鑰匙，當他被問及在哪裏丟了鑰匙時，他卻說另一方的那個黑暗角落裡。可是，為什麼他要到街燈下來找鑰匙呢？因為這裏比較亮。（〈抵禦民粹主義誘惑〉，齊澤克，查日新譯，《國外理論動態》2007 年第9 期）</p>
          <p>今日泛民、真普選聯盟以及「讓愛與和平佔領中環」就是明知出路的鑰匙在黑暗的一角，但他們卻樂此不疲在燈光下，尋找香港的出路。在燈光下找出路，不只是比較易找，更能讓更多人知道他們在為香港找出路。就以「佔領中環」運動作例子來批判，由「核彈」級數，癱瘓中環的提高政府管治成本的激烈社會行動，退卻至只談及「愛與和平」，以不阻礙經濟發展和社會運作的活動，明顯就是由「黑暗」走到「光明」的鬧劇。</p>
          <h3>全民制憲在港早有萌芽</h3>
          <p>香港人能自治、能自我守護港人利益、捍衛本土價值出路的鑰匙在哪？很明顯已不是在中共操控的基本法燈光之下，在暗角裡，我們應摸索另一片天，丟掉基本法能捍衛港人利益的幻想，準備達致全民制憲的抗爭。</p>
          <p>早在1996年，「前綫」成立（按：今日重新組成的「前綫」，綱領已刪去「全民制憲」。），倡議「全民制憲」，強調對基本法制訂的不民主，故不應如民主黨般視基本法為神聖不可侵犯，建議「地方憲法必須體現香港市民『全民制憲』的權利，港人重新制訂這部憲法的內容，而這部憲法必須經過港人『全民公決』的程序議決產生。」（許由，《全民怎樣制憲？——兼論前線的成立》，《先驅》第40期，1996年10月）</p>
          <p>然而要全港市民共同商討一部憲法，單是技術上的考量，就比戴耀廷提出「佔領中環」的一萬人商討日更匪夷所思。可是只由立法機構的代議士制訂憲法，又未免諮詢度不足；即使由政府提出法案予全港市民作公投，都只是形式上的民主，當中有不少技術細節可讓當權者蒙混過去。因此當年前綫就建議，以普選形式成立「港人代表大會」，由該會共商制定憲法方案，予港人公投方式議決。</p>
          <h3>全民制憲的具體操作</h3>
          <p>然而當年前綫的論述尚欠完整，例如他們未有解決「港人代表大會」的代議士會與中共交易、妥協，繼而大玩文字遊戲，只訂立法案的枝節予港人公決，反而為獨栽管治披上民主的外衣。除了前綫，吳恭劭與劉山青在1996年成立「全民制憲學會」, 鼓吹以全民制憲取代不民主的《基本法》。「全民制憲學會」於1999年就有關修訂基本法第 159 條作<a href='http://www.legco.gov.hk/yr98-99/chinese/panels/ca/papers/p1519c01.pdf'><i class="pt-link inline"></i>書面建議</a>，為全民制憲的具體操作提出更完整的視野。他們建議設立「制憲大會」，會議成員全部由直接普選產生。制憲大會負責基本法的重訂事務。大會通過的法案，需逐條交予全港市民以公決的方式認可。除了「制憲大會」，亦建議立法賦予「民間制憲」的權力，即任何獲得一定百分比人數聯署的修改基本法議案（美國加州的例子是5%），可交制憲大會表決。如果該議案的聯署人數所佔的百分比超過某一程度（ 美國加州的例子是8%），該議案可直接交由全港市民逐條公決。而且經公決認可的條文，香港政府必須遵守及執行，中共政府、全國人大常委均無權反對和干預。再者，他們亦建議不論中港任何機關，均沒有憲法的修改權。</p>
          <h3>冰島首創全民制憲</h3>
          <p>直至2012年10月，全球才有首個全民制憲的案例，人口30萬的冰島，在2010年由25名平民百姓組成委員會，檢視該國憲法，並在社交網站推特(Twitter)及面書(Facebook)上衡量民眾提出的建議後，設定出包括有關國家天然資源擁有權，及未來民主制度選擇等6條問題，予人民以「是」或「否」作投票。（參考<a href='http://www.facebook.com/Stjornlagarad'><i class="pt-link inline"></i>冰島「Stjórnlagaráð」全民制憲面書群組</a>）</p>
          <h3>全民制憲才是唯一出路</h3>
          <p>面對中共可任意修改、解釋基本法的情況，港人要憑藉基本法捍衞自身利益，免被中共掠奪，看來機會渺茫。然而真普選聯盟和佔領中環的發起人卻樂於在基本法的框架下爭取普選，向共產黨和平理性商討香港的出路，根本就是與虎謀皮，那不如丟棄基本法框架制紂，直接與中共對抗，要求香港自治，全民制憲，才是真正解救香港的鑰匙和出路。</p>
        </div>

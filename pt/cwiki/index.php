        <h2><span class="ch">第二章</span>中央和香港特別行政區的關係</h2>
        <ul class="basic-ch ch2 pt-dd">
          <li class="article" id="article12">
            <h4>第十二條</h4>
            <p>香港特別行政區是中華人民共和國的一個享有高度自治權的地方行政區域，直轄於中央人民政府。</p>
          </li>
          <li class="article" id="article13">
            <h4>第十三條</h4>
            <p>中央人民政府負責管理與香港特別行政區有關的外交事務。</p>
            <p>中華人民共和國外交部在香港設立機構處理外交事務。</p>
            <p>中央人民政授權香港特別行政區依照本法自行處理有關的對外事務。</p>
          </li>
          <li class="article" id="article14">
            <h4>第十四條</h4>
            <p>中央人民政府負責管理香港特別行政區的防務。</p>
            <p>香港特別行政區政府負責維持香港特別行政區的社會治安。</p>
            <p>中央人民政府派駐香港特別行政區負責防務的軍隊不干預香港特別行政區的地方事務。香港特別行政區政府在必要時，可向中央人民政府請求駐軍協助維持社會治安和救助災害。</p>
            <p>駐軍人員除須遵守全國性的法律外，還須遵守香港特別行政區的法律。</p>
            <p>駐軍費用由中央人民政府負擔。</p>
          </li>
          <li class="article" id="article15">
            <h4>第十五條</h4>
            <p>中央人民政府依照本法第四章的規定任命香港特別行政區行政長官和行政機關的主要官員。</p>
          </li>
          <li class="article" id="article16">
            <h4>第十六條</h4>
            <p>香港特別行政區享有行政管理權，依照本法的有關規定自行處理香港特別行政區的行政事務。</p>
          </li>
          <li class="article" id="article17">
            <h4>第十七條</h4>
            <p>香港特別行政區享有立法權。</p>
            <p>香港特別行政區的立法機關制定的法律須報全國人民代替大會常務委員會備案。備案不影響該法律的生效。</p>
            <p>全國人民代表大會常務委員會在徵詢其所屬的香港特別行政區基本法委員會後，如認為香港特別行政區立法機關制定的任何法律不符合本法關於中央管理的事務及中央和香港特別行政區的關係的條款，可將有關法律發回，但不作修改。經全國人民代表大會常務委員會發回的法律立即失效，除香港特別行政區的法律另有規定外，無洋溯及力。</p>
          </li>
          <li class="article" id="article18">
            <h4>第十八條</h4>
            <p>在香港特別行政區實行的法律為本法第八條規定的香港原有法律和香港特別行政區立法機關制定的法律。</p>
            <p>全國性法律除列於本法附件三者外，不在香港特別行政區實施。凡列於本法附件三之法律，由香港特別行政區在當地公布或立法實施。</p>
            <p>全國人民代表大會常務委員會在徵詢其所屬的香港特別行政區基本法委員會和香港特別行政區政府的意見後，可對列於本法附件三的法律作出增減，任何列入附件三的法律，限於有關國防、外交和其他按本法規定不屬於香港特別行政區自治範圍的法律。</p>
            <p>全國人民代表大會常務委員會決定宣布戰爭狀態或因香港特別行政區內發生香港特別行政區政府不能控制的危及國家統一或安全的動亂而決定香港特別行政區進入緊急狀態，中央人民政府可發布命令將有關全國性法律在香港特別行政區實施。</p>
          </li>
          <li class="article" id="article19">
            <h4>第十九條</h4>
            <p>香港特別行政區享有獨立的司法權和終審權。</p>
            <p>香港特別行政區法院除繼續保持香港原有法律制度和原則對法院審判權所作的限制外，對香港特別行政區所有的案件均有審判權。</p>
            <p>香港特別行政區法院對國防、外交等國家行為無管轄權。香港特別行政區法院在審理案件中遇有涉及國防、外交等國家行為的事實問題，應取得行政長官就該等問題發出的證明文件，上述文件對法院有約束力。行政長官在發出證明文件前，須取得中央人民政府的證明書。</p>
          </li>
          <li class="article" id="article20">
            <h4>第二十條</h4>
            <p>香港特別行政區可享有全國人民代表大會和全國人民代表大會常務委員會及中央人民政府授予的其他權力。</p>
          </li>

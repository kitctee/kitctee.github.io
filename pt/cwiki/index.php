      <li class="basic-inner-wrap ch ch7" id="body-ch7">
        <h2><span class="ch">第七章</span>對外事務</h2>
        <ul class="basic-ch ch7 pt-dd">
          <li class="article" id="article150">
            <h4>第一百五十條</h4>
            <p>香港特別行政區政府的代表，可作為中華人民共和國政府代表團的成員，參加由中央人民政府進行的同香港特別行政區直接有關的外交談判 。</p>
          </li>
          <li class="article" id="article151">
            <h4>第一百五十一條</h4>
            <p>香港特別行政區可在經濟、貿易、金融、航運、通訊、旅遊、文化、體育等領域以「中國香港」的名義，單獨地同世界各國、各地區及有關國際組織保持和 發展關係，簽訂和履行有關協議。</p>
          </li>
          <li class="article" id="article152">
            <h4>第一百五十二條</h4>
            <p>對以國家為單位參加的、同香港特別行政區有關的、適當領域的國際組織和國際會議，香港特別行政區政府可派遣代表作為中華人民共和國代表團的成員或以中央人民政府和上述有關國際組織或國際議會允許的身分參加，並以「中國香港」名義發表意見。</p>
            <p>香港特別行政區可以「中國香港」的名義參加不以國家為單位參加的國際組織和國際會議。對中華人民共和國已參加而香港也以某種形式參加了的國際組織，中央人民政府將採取必要措施使香港特別行政區以適當形式繼續保持在這些組織中的地位。</p>
            <p>對中華人民共和國尚未參加而香港已以某種形式參加的國際組織，中央人民政府將根據需要使香港特別行政區以適當形式繼續參加這些組織。</p>
          </li>
          <li class="article" id="article153">
            <h4>第一百五十三條</h4>
            <p>中華人民共和國締結的國際協議，中央人民政府可根據香港特別行政區的情況和需要，在徵詢香港特別行政區政府的意見後，決定是否適用於香港特別行政區。</p>
            <p>中華人民共和國尚未參加但已適用於香港的國際協議仍可繼續適用。中央人民政府根據需要授權或協助香港特別行政區政府作出適當安排，使其他有關國際協議適用於香港特別行政區。</p>
          </li>
          <li class="article" id="article154">
            <h4>第一百五十四條</h4>
            <p>中央人民政府授權香港特別行政區政府依照法律給持有香港特別行政區永久性居民身分證的中國公民簽發中華人民共和國香港特別行政區護照，給透這行政區的其他合法居留者簽發中華人民共和國香港特別行政區的其他旅行證件。上述護照和證件，前往各國和各地區有效，並載明持有人有返回香港特別行政區的權利。</p>
            <p>對世界各國或各地區的人入境、逗留和離境，香港特別行政區政府可實行出入境管制。</p>
          </li>
          <li class="article" id="article155">
            <h4>第一百五十五條</h4>
            <p>中央人民政府協助或授權香港特別行政區政府與各國或各地區締結互免簽證協議。</p>
          </li>
          <li class="article" id="article156">
            <h4>第一百五十六條</h4>
            <p>香港特別行政區可根據需要在外國設立官方或半官方的經濟和貿易機構，報中央人民政府備案。</p>
          </li>
          <li class="article" id="article157">
            <h4>第一百五十七條</h4>
            <p>外國在香港特別行政區設立領事機構或其他官方、半官方機構，須經中央人民政府批准。</p>
            <p>已同中華人民共和 國建立正式外交關係的國家在香港設立的領事機構和其他官方機構，可予保留。尚未同中華人民共和國建立正式外交關係的國家在香港設立的領事機構和其他官方機構，可根據情況允許保留或改為半官方機構。</p>
            <p>尚未為中華人民共和國承認的國家，只能在香港特別行政區設立民間機構。</p>
          </li>
        </ul>
      </li>
      <li class="basic-inner-wrap ch ch8" id="body-ch8">
        <h2><span class="ch">第八章</span>本法的解釋和修改</h2>
        <ul class="basic-ch ch8 pt-dd">
          <li class="article" id="article158">
            <h4>第一百五十八條</h4>
            <p>本法的解釋權屬於全國人民代表大會常務委員會。</p>
            <p>全國人民代表大會常務委員會授權香港特別行政區法院在審理案件時對本法關於香港特別行政區自治範圍內的條款自行解釋。</p>
            <p>香港特別行政區法院在審理案件時對本法的其他條款也付解釋。但如香港特別行政區法院在審理案件時需要對本法關於中央人民政府管理的事務或中央和香港特行政區關係的條款進行解釋，而該條款的解釋又影響到案件的判決，在對該案件作出不可上訴的終局判決前，應由香港特別行政區終審法院請全國人民代表大會常務委員會對有關條款作出解釋。如全國人民代表大會常務委員會作出解釋，香港特別行政區法院在引用該條款時，應以全國人民代表大會常務委員會的解釋為準。但在此以前作出的判決不受影響。</p>
            <p>全國人民代表大會在對本法進行解釋前，徵詢其所屬的香港特別行政區基本法委員會的意見。</p>
          </li>
          <li class="article" id="article159">
            <h4>第一百五十九條</h4>
            <p>本法的修改權屬於全國人民代表大會。</p>
            <p>本法的修改提案權屬於全國人民代表大會常務委員會、國務院和香港特別行政區。香港特別行政區的修改議案，須經香港特別行政區的全國人民代表三分之二多數、香港特別行政區立法會全體議員三分之二多數和香港特別行政區行政長官同意後，交由香港特別行政區出席全國人民代表大會的代表團向全國人民代表大會提出。</p>
            <p>本法的修改議案在列入全國人民代表大會的議程前，先由香港特別行政區基本法委員會研究並提出意見。</p>
            <p>本法的任何修改，均不得同中華人民共和國對香港既定的基本方針政策相牴觸。</p>
          </li>
        </ul>
      </li>
      <li class="basic-inner-wrap ch ch9" id="body-ch9">
        <h2><span class="ch">第九章</span>附則</h2>
        <ul class="basic-ch ch9 pt-dd">
          <li class="article" id="article160">
            <h4>第一百六十條</h4>
            <p>香港特別行政區成立時，香港原有法律除由全國人民代表大會常務委員會宣布為同本法牴觸者外，採用為香港特別行政區法律 ，如以後發現有的法律與本法牴觸，可依照本法規定程序修改或停止生效。</p>
            <p>香港原有法律下有效的文件、證件、契約和權利義務，在不牴觸本法的前提下繼續有效，受香港特別行政區的承認和保護。</p>
          </li>
        </ul>
      </li>
    </ul>
